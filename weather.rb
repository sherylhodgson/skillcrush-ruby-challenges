todays_weather = 'sunny'

case todays_weather

when 'sunny'
	puts "Hooray!  Go naked!  Who cares? The sun is OUT!"
when 'rainy'
	puts "Oh blah!  Who cares?  You're just going to get wet!"
when 'foggy'
	puts "Try and wear something loud, preferably with a flashing light on it"
when 'cloudy'
	puts "Oh I don't know - why don't you put on something colourful to brighten everyone's day?"
else
	puts "The little black dress again maybe?"
end
